//in typescript access modifers are keywords used to control the visibility and accessbility of class properties and methods. there are three access modifiers in typescript.

//public
//prvaite
//protected

//public: this is the default access modifier. properties and methods declared as public can be accessed from anywhere ,both inside and outside the class

//private: properties and methods declared as private can only be accessed within the same class. they are not accesible from outside the class.

//protected : properties and methods declared as protected can be accessed within the class and its subclasses . they are not accessible from outside the class and its subclasses.